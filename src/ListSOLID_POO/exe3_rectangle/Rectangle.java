package ListSOLID_POO.exe3_rectangle;

public class Rectangle {

    private float height;
    private float width;

    public Rectangle(){

    }

    public float getHeight() {
        return height;
    }

    public void setHeight(float height) {
        this.height = height;
    }

    public float getWidth() {
        return width;
    }

    public void setWidth(float width) {
        this.width = width;
    }
}
